function add(a,b){
    return a+b;
}
function subtract(q,b){
    return q-b;
}
function multiply(j,k){
    return j*k;
}
function divide(p,q){
    return p/q;
}
function increment(n){
    return n+1;
}
function decrement(n){
    return n-1;
}

function makeInt(n){
    return parseInt(n, 10);
}

function preserveDecimal(n){
    return parseFloat(n);
}